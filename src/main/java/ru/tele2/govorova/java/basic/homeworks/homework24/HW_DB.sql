CREATE TABLE Tests (
	test_id INTEGER NOT NULL,
	test_name TEXT(250),
	CONSTRAINT Tests_PK PRIMARY KEY (test_id)
);
-- Answers определение

CREATE TABLE Answers (
	answer_id INTEGER NOT NULL,
	question_id INTEGER,
	is_correct NOT NULL,
	answer TEXT,
	CONSTRAINT Answers_Questions_FK FOREIGN KEY (question_id) REFERENCES Questions(question_id)
);
-- Questions определение

CREATE TABLE Questions (
	question_id INTEGER NOT NULL,
	question_text TEXT(255) NOT NULL,
	test_id INTEGER NOT NULL,
	CONSTRAINT Questions_PK PRIMARY KEY (question_id),
	CONSTRAINT Questions_Tests_FK FOREIGN KEY (test_id) REFERENCES Tests(test_id)
);

